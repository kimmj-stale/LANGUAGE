package ch_02;

public class _04_EscapeSequence {
    public static void main(String[] args) {
        //특수문자 , 이스케이프 문자 (Escape Sequence)
        System.out.println("ㅓava\nPython\nKotlin");     //줄바꿈 하여 표현할 때
        System.out.println("\"");       //" 하나만 출력할 때
        System.out.println("\'");       //'하나만 출력할 때
        System.out.println("넓은\t공간");       //탭 크기 만큼 띄고 싶을 때
        System.out.println("\\");       // \ 하나만 출력할 때


    }
}
